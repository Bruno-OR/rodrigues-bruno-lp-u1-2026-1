#include <stdio.h>

int main()
{
    int n, i, cont = 0;
    printf("\n------------------------------------PRIMOS-------------------------------------------\n\n");
    printf("Digite um número: ");
    scanf("%d", &n);
    printf("\n--------------------------------------------------------------------------------------\n");
    for (i = 1; i <= n; i++)
    {
        if (n % i == 0)
        {
            cont += 1;
        };
    };
    printf("\n------------------------------------RESULTADO-----------------------------------------\n\n");
    if (cont != 2)
    {
        printf("%d não é primo", n);
        printf("\n\n---------------------------------------------------------------------------------------\n\n");
    }
    else
    {
        printf("%d é primo", n);
        printf("\n\n--------------------------------------------------------------------------------------\n\n");
    }
}