#include <stdio.h>

int main()
{

    int n, fat = 1, i;
    printf("\n------------------------------------FATORIAL-------------------------------------------\n\n");
    printf("Digite um número: ");
    scanf("%d", &n);

    for (i = 1; i <= n; i++)
    {
        fat = fat * i;
    }
    printf("\n------------------------------------RESULTADO-----------------------------------------\n\n");
    printf("%d! = %d", n, fat);
    printf("\n\n--------------------------------------------------------------------------------------\n\n");

    return 0;
}